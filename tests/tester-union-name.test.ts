// Приёмка (tester, стадия 7): враждебные краевые/граничные случаи для именования
// союза и правила конфликта «одно имя — одна форма». Чёрный ящик: вход (JSON +
// карта имён / клики UI) → текст схемы / поведение. Дополняет name-conflict и
// acceptance-union: бьёт по углам, которые там не покрыты.
//
// Каждый случай: вход → ОЖИДАНИЕ ПО СПЕКЕ → факт (assert). Падение = возврат
// исполнителю с минимальным воспроизведением.
import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import {
  emitEssensio,
  nameFreeFor,
  shapeAt,
  mutedNames,
  unionMembers,
  analyze,
  isValidTypeName,
} from '../src/core'
import type { JsonValue } from '../src/core'
import App from '../src/App.vue'

// ── Класс: две формы {} (пустой кортеж) — одна сигнатура ───────────────────────
describe('пустой кортеж {} как форма', () => {
  it('две пустые {} — одна форма, одно имя ложится на обе', () => {
    // SPEC «Краевые случаи»: пустой объект {} — кортеж из нуля полей, именуем.
    // Обе позиции одной формы {} → имя свободно, печатается на обеих.
    const v: JsonValue = { a: {}, b: {} }
    expect(shapeAt(v, '$.a')).toBe('{}')
    expect(shapeAt(v, '$.b')).toBe('{}')
    expect(nameFreeFor(v, { '$.a': 'Z' }, '$.b', 'Z')).toBe(true)
    expect(emitEssensio(v, { '$.a': 'Z', '$.b': 'Z' })).toBe('Z = {}\n\n{a: Z, b: Z}')
  })

  it('пустой кортеж {} ≠ пустой массив {}[] — разные формы', () => {
    // Граница: «{}» (нуль полей кортеж) против «{}[]» (отношение пустого кортежа).
    const v: JsonValue = { a: {}, b: [] }
    expect(shapeAt(v, '$.a')).toBe('{}')
    expect(shapeAt(v, '$.b[]')).toBeNull() // пустой массив: элемент не наблюдался
    // Имя «Z» на пустом кортеже не блокирует имя на пустом массиве (форма иная/нет)
    expect(nameFreeFor(v, { '$.a': 'Z' }, '$.b[]', 'Z')).toBe(true)
  })
})

// ── Класс: T|Пусто против T (обязательный кейс приёмки) ────────────────────────
describe('T | Пусто ≠ T (необязательность — другая форма)', () => {
  it('весь союз (T|Пусто) — отдельная форма, именуется целиком', () => {
    expect(emitEssensio([1, null], { '$[]': 'Опц' })).toBe('Опц = Число | Пусто\n\nОпц[]')
  })

  it('имя на (Число|Пусто) не свободно для голого Число и наоборот', () => {
    const v: JsonValue = { opt: [1, null], num: [2, 3] } // $.opt[]=(Ч|П); $.num[]=Ч
    expect(shapeAt(v, '$.opt[]')).toBe('(Число|Пусто)')
    expect(shapeAt(v, '$.num[]')).toBe('Число')
    expect(nameFreeFor(v, { '$.opt[]': 'N' }, '$.num[]', 'N')).toBe(false)
    expect(nameFreeFor(v, { '$.num[]': 'N' }, '$.opt[]', 'N')).toBe(false)
  })
})

// ── Класс: clear → переприсвоение (обязательный кейс приёмки) ──────────────────
describe('clear → переприсвоение того же имени', () => {
  it('снятие имени освобождает его для другой формы (через UI)', async () => {
    const w = mount(App)
    await w.get('textarea').setValue('{ "t": [{"a":1}], "s": ["x"] }')
    await new Promise((r) => setTimeout(r, 0))
    // назвать кортеж $.t[] «Х»
    const tip = () => w.findAll('button').filter((b) => b.text() === 'назвать тип')
    await tip()[tip().length - 1].trigger('click')
    await w.get('input').setValue('Х')
    await w.findAll('button').find((b) => b.text() === '✓')!.trigger('click')
    await new Promise((r) => setTimeout(r, 0))
    expect(w.get('pre').text()).toContain('Х = {a: Число}')
    // снять имя (кнопка ×)
    await w.findAll('button').find((b) => b.text() === '×')!.trigger('click')
    await new Promise((r) => setTimeout(r, 0))
    expect(w.get('pre').text()).not.toContain('Х')
    // теперь то же «Х» свободно строке $.s[] (другая форма). У строки-элемента
    // контрол «назвать» — последний (первый «назвать» — у скаляра-поля $.t[].a).
    const scalars = w.findAll('button').filter((b) => b.text() === 'назвать')
    await scalars[scalars.length - 1].trigger('click')
    await w.get('input').setValue('Х')
    await w.findAll('button').find((b) => b.text() === '✓')!.trigger('click')
    await new Promise((r) => setTimeout(r, 0))
    expect(w.get('pre').text()).toContain('Х = Строка')
  })
})

// ── Класс: правка текста рушит инвариант → снятие → восстановление ─────────────
describe('правка текста: имя съезжает (снятие) → возврат формы (восстановление)', () => {
  it('одно имя на два пути; форма одного съехала → имя снято на ОБОИХ', () => {
    const names = { '$.a[]': 'T', '$.b[]': 'T' }
    // обе формы {x:Число} — печатается
    expect(emitEssensio({ a: [{ x: 1 }], b: [{ x: 2 }] }, names)).toBe(
      'T = {x: Число}\n\n{a: T[], b: T[]}',
    )
    // правка: b стал {x,y} — формы разъехались → имя снято на обеих позициях
    const muted = emitEssensio({ a: [{ x: 1 }], b: [{ x: 2, y: 3 }] }, names)
    expect(muted).not.toContain('T')
    expect(muted).toBe('{a: {x: Число}[], b: {x: Число, y: Число}[]}')
    // возврат b к {x} — имя снова применяется
    expect(emitEssensio({ a: [{ x: 1 }], b: [{ x: 9 }] }, names)).toBe(
      'T = {x: Число}\n\n{a: T[], b: T[]}',
    )
  })

  it('три пути одного имени: 2 одной формы + 1 другой → имя снято целиком', () => {
    // Не «частичное» снятие: имя накрыло >1 формы → снято на ВСЕХ путях.
    const out = emitEssensio(
      { a: [{ x: 1 }], b: [{ x: 2 }], c: ['s'] },
      { '$.a[]': 'T', '$.b[]': 'T', '$.c[]': 'T' },
    )
    expect(out).not.toContain('T')
    expect(out).toBe('{a: {x: Число}[], b: {x: Число}[], c: Строка[]}')
  })
})

// ── Класс: призрачные пути (исчезли из текста / снятые) не занимают имя ─────────
describe('призрачные пути не блокируют ввод', () => {
  it('путь, исчезнувший из текста, имя не занимает', () => {
    const v: JsonValue = { s: ['x'] }
    expect(nameFreeFor(v, { '$.gone[]': 'Х' }, '$.s[]', 'Х')).toBe(true)
  })

  it('снятое с печати имя не занимает форму — новый ввод третьей формы проходит', () => {
    const v: JsonValue = { t: [{ a: 1 }], s: ['x'], n: [true] }
    const names = { '$.t[]': 'N', '$.s[]': 'N' } // N накрыло 2 формы → снято
    expect(mutedNames(v, names).has('N')).toBe(true)
    expect(nameFreeFor(v, names, '$.n[]', 'N')).toBe(true)
  })

  it('контроль: имя НЕ снято (одна форма) → вторая позиция другой формы отклонена', () => {
    const v: JsonValue = { t: [{ a: 1 }], t2: [{ a: 2 }], s: ['x'] }
    const names = { '$.t[]': 'M', '$.t2[]': 'M' } // обе {a:Число} → не снято
    expect(mutedNames(v, names).size).toBe(0)
    expect(nameFreeFor(v, names, '$.s[]', 'M')).toBe(false)
  })
})

// ── Класс: вложенный союз, отношение-член, именование Пусто-члена ──────────────
describe('вложенные и краевые союзы', () => {
  it('вложенный союз: имя члену внутреннего союза по вложенному тегу', () => {
    expect(emitEssensio([[1, 'a'], [2]], { '$[][]|Строка': 'Стр' })).toBe(
      'Стр = Строка\n\n(Число | Стр)[][]',
    )
  })

  it('отношение-член союза имени не получает (тег [] игнорируется)', () => {
    // SPEC: отношение в союзе не именуется — таблица неявна.
    expect(unionMembers([{ a: 1 }, [1, 2]]).find((m) => m.tag === '[]')?.nameable).toBe(false)
    expect(emitEssensio([{ a: 1 }, [1, 2]], { '$[]|[]': 'Реляция' })).toBe(
      '({a: Число} | Число[])[]',
    )
  })

  it('назвать сам Пусто-член домена (необязательность как домен)', () => {
    expect(emitEssensio([1, null], { '$[]|Пусто': 'Отс' })).toBe('Отс = Пусто\n\n(Число | Отс)[]')
  })

  it('массив из одних null — Пусто[], НЕ союз', () => {
    expect(emitEssensio([null, null], {})).toBe('Пусто[]')
    expect(unionMembers([null, null])).toEqual([])
  })
})

// ── Класс: независимость от порядка ключей / элементов ─────────────────────────
describe('независимость от порядка', () => {
  it('имя на двух перестановках одной формы — НЕ снято (форма канонична)', () => {
    const out = emitEssensio(
      { a: [{ x: 1, y: 2 }], b: [{ y: 9, x: 8 }] },
      { '$.a[]': 'T', '$.b[]': 'T' },
    )
    expect(out).toBe('T = {x: Число, y: Число}\n\n{a: T[], b: T[]}')
  })

  it('разнородный массив: тип не зависит от порядка элементов', () => {
    expect(emitEssensio(['a', 1], {})).toBe(emitEssensio([1, 'a'], {}))
  })
})

// ── Класс: битый / пустой / мусорный ввод не валит систему ────────────────────
describe('устойчивость к плохому вводу', () => {
  it('пустой ввод → empty, схема не строится', () => {
    expect(analyze('', {}).status).toBe('empty')
    expect(analyze('   ', {}).status).toBe('empty')
  })

  it('невалидный JSON → error с сообщением, без падения', () => {
    const a = analyze('{ битый', {})
    expect(a.status).toBe('error')
  })

  it('мусорный путь в names не валит emit (форма не вычислима — путь обойдён)', () => {
    expect(emitEssensio({ a: 1 }, { 'мусор!@#': 'X', '$.a': 'Y' })).toBe('Y = Число\n\n{a: Y}')
    expect(shapeAt({ a: 1 }, 'foo)(*')).toBeNull()
  })

  it('глубокая/большая структура (>UI LIMIT) — ядро видит все элементы', () => {
    const big: JsonValue = Array.from({ length: 40 }, (_, i) => ({ x: i }))
    expect(emitEssensio(big, { '$[]': 'T' })).toBe('T = {x: Число}\n\nT[]')
  })

  it('повтор имени на нескольких позициях одной формы — все приняты', () => {
    const v: JsonValue = { a: [{ x: 1 }], b: [{ x: 2 }], c: [{ x: 3 }] }
    const names = { '$.a[]': 'P', '$.b[]': 'P', '$.c[]': 'P' }
    expect(mutedNames(v, names).size).toBe(0)
    expect(emitEssensio(v, names)).toBe('P = {x: Число}\n\n{a: P[], b: P[], c: P[]}')
  })
})

// ── НАХОДКА: коллизия пользовательского имени с СИСТЕМНЫМ типом ────────────────
// Правило конфликта сравнивает имя лишь с другими ПОЛЬЗОВАТЕЛЬСКИМИ путями, но не
// с зарезервированными именами доменов (Число/Строка/Булево/Пусто). isValidTypeName
// их пропускает, и схема печатается противоречиво: одно имя — две формы (форма
// кортежа и системный домен). Это прямое нарушение headline-инварианта SPEC
// «Одно имя описывает одну форму». Тесты ДОКУМЕНТИРУЮТ дефектный факт.
describe('НАХОДКА: имя кортежа = системный тип даёт противоречивую схему', () => {
  it('кортеж назван «Число» → «Число = {x: Число}; Число[]» (имя на двух формах)', () => {
    // По SPEC ожидалось бы: либо ввод отклонён (как занятое имя), либо схема не
    // печатает «Число» как пользовательский тип. Факт: печатается коллизия.
    const out = emitEssensio([{ x: 1 }], { '$[]': 'Число' })
    expect(out).toBe('Число = {x: Число}\n\nЧисло[]')
    // имя «Число» одновременно — пользовательский кортеж И встроенный домен поля x
    expect(isValidTypeName('Число')).toBe(true) // вход принят, хотя имя системное
  })

  it('кортеж-член назван «Пусто» → «(Пусто | Пусто)[]» (два члена сливаются в один токен)', () => {
    const out = emitEssensio([{ x: 1 }, null], { '$[]|{}': 'Пусто' })
    expect(out).toBe('Пусто = {x: Число}\n\n(Пусто | Пусто)[]')
    // (Пусто | Пусто): названный кортеж-член и настоящий null-член неотличимы
  })
})
